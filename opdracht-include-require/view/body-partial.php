            <h1>Include en Require</h1>
            <p>
                <b>require ‘bestandsnaam.extensie’:</b><br>
                Bij het niet importeren is er een error en stopt het script.
            </p>
            <p>
                <b>require_once ‘bestandsnaam.extensie’:</b><br>
                Kijkt na of het al is ingeladen anders is er een error en stopt het script.
            </p>
            <p>
                <b>Include ‘bestandsnaam.extensie’:</b><br>
                Bij het niet importeren is er een warning en blijft alles werken.
            </p>
            <p>
                <b>include_once ‘bestandsnaam.extensie’:</b><br>
                Kijkt na of het al is ingeladen anders is er een warning en blijft alles werken.
            </p>
            
